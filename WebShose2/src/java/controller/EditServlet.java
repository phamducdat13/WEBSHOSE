    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int productid = Integer.parseInt(request.getParameter("id"));
        String pname = request.getParameter("name");
        String pimage = request.getParameter("image");
        double pprice = Double.parseDouble(request.getParameter("price"));
        String ptitle = request.getParameter("title");
        String pdescription = request.getParameter("description");
        int pquanlity = Integer.parseInt(request.getParameter("quanlity"));
        int pcategory = Integer.parseInt(request.getParameter("category"));
        ProductDAO productData = new ProductDAO();
        productData.EditProduct(pname, pimage, pprice, ptitle, pdescription, pquanlity, pcategory, productid);
        response.sendRedirect("manager");
    }

